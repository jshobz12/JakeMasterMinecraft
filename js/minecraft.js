mineCraft.start = function () {
    $("#closeTheShovelModal").on("click",function(){
        $("#shovelNotAvailable").hide();
    })
    $('#myModal').modal('show');
    //Click on element of the grid
    $(".elem").on("click", function (e) {
        //can add element only on something(no bluesky)
        var coordinate = $(this).attr("id").split("_");
        console.log(coordinate)
        var checkDown = parseInt(coordinate[0]);
        checkDown += 1;
        coordinate[0] = checkDown;
        coordinate = coordinate.join("_");
        console.log(coordinate);

        if (!($("#" + coordinate).hasClass("blueSky")) && $(this).hasClass("blueSky") && $("#lastElem").attr('class') && !mineCraft.toolSelected) {
            $(this).removeClass("elem blueSky");
            $(this).addClass("elem " + mineCraft.type);


        }
        //add bluesky class if tool is selected
        else if (mineCraft.toolSelected) {
            if ($(this).hasClass(mineCraft.type)) {
                $(this).removeClass(mineCraft.type);
                $(this).addClass("blueSky");
                $("#lastElem").removeClass($("#lastElem").attr("class"));
                $("#lastElem").addClass("elem " + mineCraft.type);

            }
            else if ($(this).hasClass(mineCraft.type2)) {
                $(this).removeClass(mineCraft.type2);
                $(this).addClass("blueSky");
                $("#lastElem").removeClass($("#lastElem").attr("class"));
                $("#lastElem").addClass("elem " + mineCraft.type2);
            }
        }
    })
    //click on the last element clicked
    $("#lastElem").on("click", function (e) {
        mineCraft.type = ($('#lastElem').attr('class').split(' ')[1]);
        mineCraft.toolSelected = false;
        document.body.style.cursor = "url(./images/Hand_of_god.cur),auto";

    })
    //select menu

    $("#default").on("click", function () {
        $("#container").html("");
        mineCraft.menu = "default";
        mineCraft.createMap(mineCraft.defaultTheme);
        mineCraft.start();
        $('#myModal').modal('hide');
        mineCraft.attributeMenu();
    })

    $("#snowTheme").on("click", function () {

        $("#container").html("");
        mineCraft.menu = "snow";
        mineCraft.createMap(mineCraft.snowTheme);
        mineCraft.start();
        $('#myModal').modal('hide');
        mineCraft.attributeMenu();
    })

    $("#desertTheme").on("click", function () {

        $("#container").html("");
        mineCraft.menu = "gold";
        mineCraft.createMap(mineCraft.goldTheme);
        mineCraft.start();
        $('#myModal').modal('hide');
        mineCraft.attributeMenu();
    })

    $("#mordorTheme").on("click", function () {
        $('#myModal').modal('hide');
        $("#container").html("");
        mineCraft.menu = "mordor"
        mineCraft.createMap(mineCraft.mordorTheme);
        mineCraft.start();
        $('#myModal').modal('hide');
        mineCraft.attributeMenu();
    })
}

